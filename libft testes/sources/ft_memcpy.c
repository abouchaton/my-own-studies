void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	unsigned
		char *s;
	unsigned
		char *d;
	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
